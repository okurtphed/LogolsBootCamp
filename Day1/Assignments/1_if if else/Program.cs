using System;

namespace _1_if_if_else {
    class Program {
        static void Main (string[] args) {

            int status = 1;

            if (status == 1) {
                Console.WriteLine ("Alive");
            } else if (status == 2)

            {
                Console.WriteLine ("Zombie");
            } else if (status == 3) {
                Console.WriteLine ("Dead");
            } else

            {
                Console.WriteLine ("Unknown");
            }
            

        }
    }
}